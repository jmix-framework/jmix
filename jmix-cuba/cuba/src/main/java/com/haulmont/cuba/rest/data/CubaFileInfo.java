package com.haulmont.cuba.rest.data;

import java.util.UUID;

public class CubaFileInfo {
    protected UUID id;
    protected String name;
    protected long size;

    public CubaFileInfo(UUID id, String name, long size) {
        this.id = id;
        this.name = name;
        this.size = size;
    }

    public UUID getId() {
        return id;
    }

    public void setId(UUID id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public long getSize() {
        return size;
    }

    public void setSize(long size) {
        this.size = size;
    }
}
