package io.jmix.charts.model.animation;

import io.jmix.charts.model.JsonEnum;

public enum AnimationEffect implements JsonEnum {
    EASE_OUT_SINE("easeOutSine"),
    EASE_IN_SINE("easeInSine"),
    ELASTIC("elastic"),
    BOUNCE("bounce");

    private String id;

    AnimationEffect(String id) {
        this.id = id;
    }

    public String getId() {
        return id;
    }

    @Override
    public String toString() {
        return id;
    }
}